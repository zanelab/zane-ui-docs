<script lang="ts" setup>
import { ref } from 'vue';

const before = ref(true);
const activeNames = ref(['1']);
const loading = ref(false);

const beforeCollapse = (): Promise<boolean> => {
  loading.value = true;
  return new Promise((resolve) => {
    setTimeout(() => {
      loading.value = false;
      return resolve(before.value);
    }, 1000);
  });
};
</script>
<!-- eslint-disable vue/html-self-closing -->
<template>
  <div v-loading="loading" class="demo-collapse">
    <div class="mb-4 flex items-center">
      <span class="mr-4">before collapse return: </span>
      <zane-switch
        :value="before"
        :inactive-value="false"
        :active-value="true"
        inactive-text="false"
        active-text="true"
      ></zane-switch>
    </div>

    <zane-collapse :value="activeNames" :before-collapse="beforeCollapse">
      <zane-collapse-item title="Consistency" name="1">
        <div>
          Consistent with real life: in line with the process and logic of real
          life, and comply with languages and habits that the users are used to;
        </div>
        <div>
